class Recipe < ActiveRecord::Base
	acts_as_xlsx
	has_many :photos, as: :imageable, dependent: :destroy
	accepts_nested_attributes_for :photos, allow_destroy: true
	has_many :steps
	accepts_nested_attributes_for :steps, :reject_if => lambda { |h| h[:instruction].blank? }, allow_destroy: true

	extend FriendlyId
	friendly_id :title, use: :slugged

	include Workflow

	workflow do
		state :draft do
			event :submit, transition_to: :pending_review
			event :approve, transition_to: :approved
			event :publish, transition_to: :published
		end

		state :pending_review do
			event :approve, transition_to: :approved
			event :reject, transition_to: :draft
			event :publish, transition_to: :published
		end

		state :approved do
			event :publish, transition_to: :published
			event :submit, transition_to: :pending_review
			event :reject, transition_to: :draft
		end

		state :published do
			event :unpublish, transition_to: :draft
		end
	end

	def self.to_csv
		CSV.generate do |csv|
			csv << column_names
			all.each do |item|
				csv << item.attributes.values_at(*column_names)
			end
		end
	end

	def self.import(file)
		CSV.foreach(file.path, headers: true) do |row|
			item = find_by_id(row["id"]) || new
			item.attributes = row.to_hash.slice(*self.column_names)
			item.save!
		end
	end
end
